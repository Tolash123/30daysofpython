# checking for the ASCII code
lowercase = ['a', 'b', 'c', 'd']
empty_list = []
for i in lowercase:
    empty_list += [ord(i)]
    print(empty_list)

# using chr to check for the character

number = [65,66,67,68]
e_list = []
for i in number:
    e_list += [chr(i)]
    print(e_list)

